for lang_code, filename in lang_files:
    filepath = os.path.join(BASE_DIR, filename)

    # Read the file
    with open(filepath, 'r', encoding='utf-8') as f:
        content = f.read()

    # Parse to find existing keys
    import xml.etree.ElementTree as ET
    tree = ET.parse(filepath)
    root = tree.getroot()
    existing_keys = set()
    for data in root.findall('data'):
        name = data.get('name')
        if name:
            existing_keys.add(name)

    # Build the XML block for missing keys
    lines = []
    lines.append("")
    lines.append("  <!-- ═══ New v3.1 Keys ═══ -->")

    added_count = 0
    for key, lang_dict in missing_keys:
        if key in existing_keys:
            continue

        val = lang_dict.get(lang_code, '')
        if not val:
            continue

        # Escape XML special chars in value
        val_escaped = escape_xml(val)

        # Check if multiline
        if '\n' in val_escaped:
            lines.append(f'  <data name="{key}" xml:space="preserve"><value>{val_escaped}</value></data>')
        else:
            lines.append(f'  <data name="{key}" xml:space="preserve"><value>{val_escaped}</value></data>')

        added_count += 1

    lines.append("")

    # Insert before </root>
    new_block = "\n".join(lines)
    content = content.replace("\n</root>", f"{new_block}\n</root>")

    # Write back
    with open(filepath, 'w', encoding='utf-8') as f:
        f.write(content)

    print(f"{filename}: added {added_count} keys (lang={lang_code})")

